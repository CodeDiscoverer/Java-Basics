package DSA;

public class TimeComplex1 {
    public static void main(String[] args) {

        int n = 1, j = 0;

        while (true) {
            switch (n) {
                case 1: {
                    if (j > 10) {
                        break;
                    } else {
                        j++;
                    }
                    break; // break out of the switch statement
                }
            }
            if (j > 10) {
                break; // break out of the while loop
            }
        }
    }
}
